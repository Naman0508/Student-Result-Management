    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo= jTextField1.getText();
        try{
            Connection con= ConnectionProviderCode.getCon();
            Statement st= con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs= st.executeQuery("Select * from student where rollNo='"+rollNo+"'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered Roll No doesn't exists");
            }else{
            jTextField1.setEditable(false);
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo= jTextField1.getText();
        int physics= Integer.parseInt(jTextField2.getText());
        int maths= Integer.parseInt(jTextField3.getText());
        int chem= Integer.parseInt(jTextField4.getText());
        int electrical= Integer.parseInt(jTextField5.getText());
        int bio= Integer.parseInt(jTextField6.getText());
        int total= physics+maths+chem+electrical+bio;
        try{
            Connection con= ConnectionProviderCode.getCon();
            Statement st= con.createStatement();
            st.executeUpdate("insert into result values('"+rollNo+"','"+physics+"','"+maths+"','"+chem+"','"+electrical+"','"+bio+"','"+total+"')");
            JOptionPane.showMessageDialog(null, "Result added successfully");
            setVisible(false);
            AddResult frame= new AddResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

