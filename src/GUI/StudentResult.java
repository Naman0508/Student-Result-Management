    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo= rollNoTextField.getText();
        try{
            Connection con= ConnectionProviderCode.getCon();
            Statement st= con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs= st.executeQuery("select * from student inner join result where student.rollNo='"+rollNo+"'and result.rollNo='"+rollNo+"'");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Invalid Roll No");
            }
            else{
                nameTextField.setText(rs.getString(1));
                genTextField.setText(rs.getString(3));
                fatherTextField.setText(rs.getString(4));
                courseTextField.setText(rs.getString(5));
                branchTextField.setText(rs.getString(6));
                PhyTextField.setText(rs.getString(8));
                MathsTextField.setText(rs.getString(9));
                ChemTextField.setText(rs.getString(10));
                ElecTextField.setText(rs.getString(11));
                BioTextField.setText(rs.getString(12));
                TotalTextField.setText(rs.getString(13));
                
                rollNoTextField.setEditable(false);
                nameTextField.setEditable(false);
                genTextField.setEditable(false);
                fatherTextField.setEditable(false);
                courseTextField.setEditable(false);
                branchTextField.setEditable(false);
                PhyTextField.setEditable(false);
                MathsTextField.setEditable(false);
                ChemTextField.setEditable(false);
                ElecTextField.setEditable(false);
                BioTextField.setEditable(false);
                TotalTextField.setEditable(false);
                
                int p= Integer.parseInt(rs.getString(8));
                int m= Integer.parseInt(rs.getString(9));
                int ch= Integer.parseInt(rs.getString(10));
                int el= Integer.parseInt(rs.getString(11));
                int bio= Integer.parseInt(rs.getString(12));
                if(p>=30 && m>=30 && ch>=30 && el>=30 && bio>=30){
                    PassFailTextField.setText("PASS");
                }else{
                    PassFailTextField.setText("Fail");
                }
                PassFailTextField.setEditable(false);
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

